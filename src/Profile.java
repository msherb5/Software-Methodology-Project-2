public class Profile {
    private String fname;
    private String lname;

    public String getFname() {
        return fname;
    }

    public void setFname(String newFName){
        this.fname = newFName;
    }

    public String getLname() {
        return lname;
    }

    public void setLname(String newLName){
        this.lname = newLName;
    }

}